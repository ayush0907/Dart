void main() {
  String grade="B";
  
  switch(grade){
    case 'A' : 
      print("Excellent grade !!!") ;
      break;
    case 'B': 
      print("Good grades");
      break;
    case 'C':
      print("Work Hard");
      break;
    case 'D':
      print("Failed");
      break;
      default :
      print("Invalid Grade");
  }
}
